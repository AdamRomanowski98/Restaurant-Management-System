    private void TotallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotallActionPerformed
        double b=Integer.parseInt(Beef.getText());
        double bu=Integer.parseInt(Buns.getText());
        double let=Integer.parseInt(Lettuce.getText());          //passing the values from textfields to double variables so they can be calculated
        double chee=Integer.parseInt(Cheese.getText());
        
        double total = (b*beef)+(bu*buns)+(let*lettuce)+(chee*cheese);    //counting tne price of the order
        String order = String.format("%.2f", total);    //result rounded to 2 decimal places
        Total.setText(order);  //setting "Total" textfield into total price of the order 
       
    }//GEN-LAST:event_TotallActionPerformed

