    void readFile(){
        try { //checks if orders.txt exists
            FileReader read = new FileReader(file+"\\orders.txt"); //FileReader checks if orders.txt exists in provided path
            System.out.println("File exists!");
        } catch (FileNotFoundException ex) { //if orders.txt doesn't exists this part of code creates it
            try {
                FileWriter write = new FileWriter(file+"\\orders.txt"); //orders.txt creation
                System.out.println("File created");
            } catch (IOException ex1) {
                Logger.getLogger(Java_Form_Login.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
        
        
    } // creation of txt in which we gonna store the data
    
    void addData(String date, double b, double bun, double lett, double chee, double total){ //adding data
        try {
            RandomAccessFile rw = new RandomAccessFile(file+"\\orders.txt", "rw"); //allows to write and read from the file("rw" - Read and write mode)
            for(int i=0;i<lines;i++){ //There are multiple datas in the file so this loop allows the program to go throught each line of text
                rw.readLine();
                
            }
           
            
            rw.writeBytes("\r\n");                            // 
            rw.writeBytes("\r\n");      
            rw.writeBytes("Date: "+date+ "\r\n");
            rw.writeBytes("Beef: "+b+ "\r\n");           //Writing string input into the file 
            rw.writeBytes("Buns:"+bun+ "\r\n");          //
            rw.writeBytes("Lettuce:"+lett+ "\r\n" );
            rw.writeBytes("Cheese:"+chee+ "\r\n" );
            rw.writeBytes("Total:"+total+"$" );
            
            
           
          
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Java_Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Java_Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    } //method to add data to into the file
    
    void Count(){
        try {
            lines=1;
            RandomAccessFile rw = new RandomAccessFile(file+"\\orders.txt", "rw");
            for(int i=0; rw.readLine() !=null;i++){
                lines++;
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Java_Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Java_Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    } //method to count the lines
    
    
    
    
    

